
public class People {
	private String ID;
	private String name;
	private String surname;
	private String phone;
	
	People(String ID,String name,String surname,String phone){
		this.ID=ID;
		this.name=name;
		this.surname=surname;
		this.phone=phone;
	}
	public String getID() {
		return ID;
	}
	
	public String getName() {
		return name;
	}
	
	public String getSurname() {
		return surname;
	}
	
	public String getPhone() {
		return phone;
	}
	
	
	
	
}
